package poly;

public class Operation {
	
	public int sum(int a, int b) {
		System.out.println("first method:");
		return (a+b);
	}

	public int sum(int a, int b, int c) {
		System.out.println("second method:");
		return (a+b+c);
	}
	
	public double sum( double a, double b, double c) {
		System.out.println("second method:");
		return (a+b+c);
	}
}
